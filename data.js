// ✅ KEY FIX: loadBackupData() runs FIRST so buttons work immediately
// Then live fetch runs in background and upgrades the data if successful
function loadTLEData() {
    loadBackupData(); // ← INSTANT, runs right away

    fetch("/tledata")
        .then(function(res) { return res.json(); })
        .then(function(data) {
            if (Array.isArray(data) && data.length > 0) {
                var first = data[0];
                var nk  = first.OBJECT_NAME ? "OBJECT_NAME" : "name";
                var l1k = first.TLE_LINE1   ? "TLE_LINE1"   : "LINE1";
                var l2k = first.TLE_LINE2   ? "TLE_LINE2"   : "LINE2";
                satellites = data.slice(0, 500).map(function(o) {
                    return { name: o[nk], l1: o[l1k], l2: o[l2k] };
                });
                document.getElementById("status").innerText =
                    "🟢 LIVE — " + satellites.length + " real debris objects | " + new Date().toLocaleTimeString();
            }
        })
        .catch(function() {
            console.log("Live fetch failed, backup already loaded");
        });

    return Promise.resolve(); // ← returns immediately, doesn't wait
}

function startAutoUpdate() {
    if (autoUpdateInterval) clearInterval(autoUpdateInterval);
    autoUpdateInterval = setInterval(function() { loadTLEData(); }, 5 * 60 * 1000);
}

function stopAutoUpdate() {
    if (autoUpdateInterval) {
        clearInterval(autoUpdateInterval);
        autoUpdateInterval = null;
        document.getElementById("status").innerText = "⏹ Auto-update stopped";
    }
}
